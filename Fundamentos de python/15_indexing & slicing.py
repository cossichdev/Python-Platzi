
text = "Somos los mejores!"
print(text) #se imprime el string text
print(text[0]) #se imprime el carácter que esta en la posición 0 del string text
print(text[10]) #se imprime el carácter que esta en la posición 10 del string text
print(text[-1]) # se imprime el Ultimo carácter del string text

print(text[:5]) #Se imprime el string text desde la posición 0 hasta la posición 5

print(text[10:15])# Se imprime el string text desde la posición 10 hasta la posición 15

print(text[:10]) #Se imprime el string text desde la posición 0 hasta la posición 10

print(text[5:-1]) #Se imprime el string text desde la posición 5 hasta la posición -1

print(text[5:]) #Se imprime el string text desde la posición 5 hasta el final
print(text[:]) #Se imprime el string text desde la posición 0 hasta el final

print(text[10:16:1]) #Se imprime el string text desde la posición 10 hasta la posición 16, con un paso de 1

print(text[10:16:2]) #Se imprime el string text desde la posición 10 hasta la posición 16, con un paso de 2

print(text[::2]) #Se imprime el string text desde la posición 0 hasta el final, con un paso de 2